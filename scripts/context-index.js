function checkHarness(context) {
  const failures = [];
  const warnings = [];
  const agents = readTextSafe(agentsFile);
  const now = readTextSafe(path.join(root, "NOW.md"));
  const plan = readTextSafe(path.join(root, "PLAN.md"));

  const requiredSections = [
    "Project",
    "Structure",
    "Workflow",
    "Language",
    "Relationships",
    "Flagged Ambiguities",
    "Learned Patterns",
  ];

  if (!hasSchema(context)) failures.push("CONTEXT.md must use context-harness schema v3.");

  if (!agents.trim()) failures.push("AGENTS.md is missing.");
  else if (!hasSchema(agents)) failures.push("AGENTS.md must use context-harness schema v3.");

  if (agents.trim()) {
    const agentLines = agents.trimEnd().split("\n").length;
    const nonIndex = stripIndex(agents);
    if (agentLines > 80) {
      warnings.push(`AGENTS.md has ${agentLines} lines; keep it to the Context Contract plus generated Context Index.`);
    }
    if (looksLikeDetailedOperatingBrief(nonIndex)) {
      warnings.push("AGENTS.md appears to contain durable operating context; move project facts to CONTEXT.md.");
    }
  }

  for (const heading of requiredSections) {
    if (!hasHeading(context, 2, heading)) failures.push(`CONTEXT.md is missing ## ${heading}.`);
  }
  if (!hasHeading(context, 2, "Operating Constraints")) {
    failures.push("CONTEXT.md is missing ## Operating Constraints.");
  }

  if (agents.trim()) {
    if (!agents.includes(START) || !agents.includes(END)) {
      failures.push("AGENTS.md is missing the generated Context Index block.");
    } else if (!indexIsFresh(agents, context)) {
      failures.push("AGENTS.md Context Index is stale; run `node scripts/context-index.js update`.");
    }
  }

  checkLibraryFresh(context, failures, warnings);

  if (now.trim()) {
    const nowLines = now.trimEnd().split("\n").length;
    if (nowLines > 20) warnings.push(`NOW.md has ${nowLines} lines; consider a Dream/Compact pass.`);
  }

  if (plan.trim()) {
    const planLines = plan.trimEnd().split("\n").length;
    if (planLines > 150) warnings.push(`PLAN.md has ${planLines} lines; consider a Dream/Compact pass.`);
  }

  for (const warning of warnings) console.log(`WARN ${warning}`);
  if (failures.length) {
    for (const failure of failures) console.log(`FAIL ${failure}`);
    process.exit(1);
  }
  console.log("OK context-harness check passed.");
}

function hasSchema(text) {
  return /<!--\s*context-harness:schema\s+v3\s*-->/.test(text);
}

function stripIndex(text) {
  const start = text.indexOf(START);
  const end = text.indexOf(END);
  if (start === -1 || end === -1 || end <= start) return text;
  return `${text.slice(0, start)}${text.slice(end + END.length)}`;
}

function looksLikeDetailedOperatingBrief(text) {
  const durableHeadings = [
    "Current Status",
    "Project Mission",
    "Near-Term Priorities",
    "Guiding Principles",
    "High-Level Timeline",
    "Communication Style",
    "Reusable Templates",
    "Recent Key Achievements",
  ];
  const headingHits = durableHeadings.filter((heading) => hasHeading(text, 2, heading) || hasHeading(text, 3, heading)).length;
  if (headingHits >= 2) return true;

  return /detailed durable project context remains in `?AGENTS\.md`?/i.test(text)
    || /AGENTS\.md.*(detailed|primary).*operating/i.test(text)
    || /comprehensive context for AI agents/i.test(text);
}

function hasHeading(markdown, level, heading) {
  const hashes = "#".repeat(level);
  const escaped = heading.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return new RegExp(`^${hashes}\\s+${escaped}\\s*$`, "m").test(markdown);
}

function indexIsFresh(agents, context) {
  const expected = renderIndex(context).trim();
  const start = agents.indexOf(START);
  const end = agents.indexOf(END);
  if (start === -1 || end === -1 || end <= start) return false;
  const headingStart = agents.lastIndexOf("## Context Index", start);
  const actualStart = headingStart === -1 ? start : headingStart;
  const actual = agents.slice(actualStart, end + END.length).trim();
  return actual === expected;
}

function checkLibraryFresh(context, failures, warnings) {
  const libraryExists = fs.existsSync(indexFile) || fs.existsSync(cardsDir) || fs.existsSync(chunksDir);
  if (!libraryExists) return;

  const manifest = readJSONSafe(indexFile);
  if (!manifest) {
    failures.push(".context-harness/index.json is missing or invalid; run `node scripts/context-index.js update`.");
    return;
  }

  if (manifest.schema !== LIBRARY_SCHEMA) warnings.push(".context-harness/index.json uses an unknown schema version.");
  const expectedHash = sourceHash(readSourceFiles(context));
  if (manifest.source_hash !== expectedHash) {
    failures.push(".context-harness/index.json is stale; run `node scripts/context-index.js update`.");
  }

  for (const card of manifest.cards || []) {
    if (card.card_path && !fs.existsSync(path.join(root, card.card_path))) {
      failures.push(`Missing context card ${card.card_path}; run \`node scripts/context-index.js update\`.`);
      break;
    }
    if (card.chunk_path && !fs.existsSync(path.join(root, card.chunk_path))) {
      failures.push(`Missing context chunk ${card.chunk_path}; run \`node scripts/context-index.js update\`.`);
      break;
    }
  }
}

function parseSections(context) {
  const sections = [];
  let currentH2 = null;
  for (const line of context.split("\n")) {
    const match = line.match(/^(##|###)\s+(.+?)\s*$/u);
    if (!match) continue;
    const section = {
      level: match[1].length,
      title: match[2].replace(/`/g, ""),
      anchor: anchorFor(match[2]),
      children: [],
    };
    if (section.level === 2) {
      sections.push(section);
      currentH2 = section;
    } else if (section.level === 3 && currentH2) {
      currentH2.children.push(section);
      sections.push(section);
    }
  }
  return sections;
}

function parseTopSections(markdown, file) {
  const lines = markdown.split("\n");
  const sections = [];
  let current = null;

  for (let i = 0; i < lines.length; i += 1) {
    const match = lines[i].match(/^(##)\s+(.+?)\s*$/u);
    if (!match) continue;
    if (current) {
      current.endLine = i;
      current.body = lines.slice(current.bodyStartIndex, i).join("\n").trimEnd();
      sections.push(current);
    }
    const title = match[2].replace(/`/g, "");
    current = {
      file,
      level: 2,
      title,
      anchor: uniqueAnchor(title, sections),
      startLine: i + 2,
      bodyStartIndex: i + 1,
      endLine: lines.length,
      body: "",
      children: [],
    };
  }

  if (current) {
    current.body = lines.slice(current.bodyStartIndex).join("\n").trimEnd();
    sections.push(current);
  }

  return sections.map((section) => ({
    ...section,
    lineCount: countLines(section.body),
    charCount: section.body.length,
    children: childHeadings(section.body),
  }));
}

function childHeadings(body) {
  return body
    .split("\n")
    .map((line) => line.match(/^###\s+(.+?)\s*$/u))
    .filter(Boolean)
    .map((match) => match[1].replace(/`/g, ""));
}

function describeSection(context, section) {
  if (PURPOSES[section.title]) return PURPOSES[section.title];
  const body = readSection(context, section.title);
  const first = body
    .split("\n")
    .map((line) => line.trim())
    .find((line) => line && !line.startsWith("<!--") && !line.startsWith("```"));
  return first ? truncate(cleanMarkdown(first), 90) : "context section";
}

function printSection(context, heading) {
  if (!heading) {
    console.error("Provide a section heading.");
    process.exit(1);
  }
  const body = readSection(context, heading);
  if (!body.trim()) {
    console.error(`Section not found: ${heading}`);
    process.exit(1);
  }
  process.stdout.write(`## ${heading}\n${body.trimEnd()}\n`);
}

function queryIndex(context, query) {
  const terms = parseTerms(query);
  if (!terms.length) {
    console.error("Provide a query term.");
    process.exit(1);
  }

  const matches = parseSections(context)
    .filter((section) => section.level === 2)
    .map((section) => {
      const body = readSection(context, section.title);
      const haystack = `${section.title}\n${body}`.toLowerCase();
      return { section, body, hit: terms.every((term) => haystack.includes(term)) };
    })
    .filter((entry) => entry.hit);

  if (!matches.length) {
    console.log("No matching CONTEXT.md sections.");
    return;
  }

  for (const { section, body } of matches) {
    console.log(`- CONTEXT.md#${section.anchor} (${section.title})`);
    const snippet = body
      .split("\n")
      .map((line) => cleanMarkdown(line.trim()))
      .find((line) => line && terms.some((term) => line.toLowerCase().includes(term)));
    if (snippet) console.log(`  ${truncate(snippet, 120)}`);
  }
}

