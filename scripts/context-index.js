function main() {
  const context = readTextSafe(contextFile);
  if (!context) {
    console.error("CONTEXT.md not found.");
    process.exit(1);
  }

  if (command === "update") {
    updateAgents(context);
    return;
  }

  if (command === "list") {
    process.stdout.write(renderIndex(context));
    return;
  }

  if (command === "section") {
    printSection(context, arg);
    return;
  }

  if (command === "query") {
    queryIndex(context, arg);
    return;
  }

  if (command === "stats") {
    printStats(context);
    return;
  }

  if (command === "hydrate") {
    hydrateContext(context, arg);
    return;
  }

  if (command === "check") {
    checkHarness(context);
    return;
  }

  console.error("Usage: node scripts/context-index.js [update|list|query|section|stats|hydrate|check] [text]");
  process.exit(1);
}

function updateAgents(context) {
  writeContextLibrary(context);
  const current = readTextSafe(agentsFile) || defaultAgents();
  const block = renderIndex(context).trimEnd();
  const next = replaceManagedBlock(current, block);
  fs.writeFileSync(agentsFile, `${next.trimEnd()}\n`);
  console.log(`Updated ${path.relative(root, agentsFile)} and .context-harness/index.json from context files.`);
}

function renderIndex(context) {
  const sections = parseSections(context).filter((section) => section.level === 2);
  const lines = [
    "## Context Index",
    START,
    "Generated by `node scripts/context-index.js update`; keep `CONTEXT.md` small enough to read with `NOW.md`.",
    "Use `node scripts/context-index.js hydrate \"<task>\"` to select cards before opening `PLAN.md`, chunks, or bulky sections.",
    "",
    "- `NOW.md` - read first on start/resume.",
    "- `CONTEXT.md` - read directly while it remains concise; compact it when it stops fitting the always-read layer.",
    "- `.context-harness/index.json` - machine retrieval manifest; do not read wholesale.",
  ];

  for (const section of sections) {
    const childText = section.children.length
      ? ` Sub: ${section.children.map((child) => child.title).join(", ")}.`
      : "";
    lines.push(`- \`CONTEXT.md#${section.anchor}\` - ${describeSection(context, section)}.${childText}`);
  }

  lines.push(END);
  return `${lines.join("\n")}\n`;
}

function replaceManagedBlock(text, block) {
  const start = text.indexOf(START);
  const end = text.indexOf(END);
  if (start !== -1 && end !== -1 && end > start) {
    const prefix = text.slice(0, start);
    const headingMatch = prefix.match(/(?:^|\n)(?:## Context Index\s*\n\s*)+$/u);
    const replaceStart = headingMatch
      ? prefix.length - headingMatch[0].length + (headingMatch[0].startsWith("\n") ? 1 : 0)
      : start;
    const before = text.slice(0, replaceStart).replace(/[ \t]*$/u, "");
    const after = text.slice(end + END.length).replace(/^\s*/u, "\n");
    return `${before}${block}${after}`;
  }
  return `${text.trimEnd()}\n\n${block}`;
}

